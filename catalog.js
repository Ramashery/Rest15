// Это финальный, окончательный тест.
// Он не подключается к базе данных. Он не загружает данные.
// Его единственная цель - доказать, что этот скрипт может найти и изменить элемент на странице.

const pageTitleElement = document.querySelector('.page-title');

if (pageTitleElement) {
    // Если элемент H1 с классом 'page-title' найден...
    pageTitleElement.textContent = "ТЕСТ УСПЕШЕН: JAVASCRIPT РАБОТАЕТ";
    pageTitleElement.style.color = "lime"; // Делаем текст ярко-зеленым, чтобы это было невозможно не заметить
} else {
    // Если даже этот элемент не найден...
    alert("КРИТИЧЕСКАЯ ОШИБКА: Скрипт запустился, но не смог найти заголовок H1 с классом 'page-title'. HTML-файл поврежден или загружается некорректно.");
}